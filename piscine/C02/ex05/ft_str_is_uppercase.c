#include <unistd.h>

int		ft_is_upperalpha(char c)
{
	if (c >= 'A' && c <= 'Z')
		return (1);
	return (0);
}

int		ft_str_is_uppercase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		if (ft_is_upperalpha(str[i++]) == 0)
			return (0);
	return (1);
}
